const magnifierGlass = document.getElementById('magnifierGlass');
const imgWrap = document.getElementById('mainImgWrap');
const ZOOM = 3;
const GLASS_W = 160, GLASS_H = 160;

function isTouch(){ return window.matchMedia('(hover:none),(pointer:coarse)').matches; }

function setupMagnifier() {
  if(!magnifierGlass || !mainImg) return;
  magnifierGlass.style.backgroundImage = `url('${mainImg.src}')`;
  magnifierGlass.style.backgroundSize = `${mainImg.width * ZOOM}px ${mainImg.height * ZOOM}px`;
}

if(mainImg) mainImg.addEventListener('load', setupMagnifier);
window.addEventListener('resize', setupMagnifier);
setTimeout(setupMagnifier, 300);

if(imgWrap) {
  imgWrap.addEventListener('mousemove', e=>{
    if(isTouch() || !magnifierGlass) return;
    const rect = imgWrap.getBoundingClientRect();
    let x = e.clientX - rect.left;
    let y = e.clientY - rect.top;
    x = Math.max(GLASS_W/2, Math.min(rect.width - GLASS_W/2, x));
    y = Math.max(GLASS_H/2, Math.min(rect.height - GLASS_H/2, y));
    magnifierGlass.style.left = (x - GLASS_W/2) + 'px';
    magnifierGlass.style.top  = (y - GLASS_H/2) + 'px';
    magnifierGlass.style.display = 'block';
    const bx = x * ZOOM - GLASS_W/2;
    const by = y * ZOOM - GLASS_H/2;
    magnifierGlass.style.backgroundPosition = `-${bx}px -${by}px`;
    magnifierGlass.style.backgroundSize = `${imgWrap.offsetWidth * ZOOM}px ${imgWrap.offsetHeight * ZOOM}px`;
  });
  imgWrap.addEventListener('mouseleave', ()=>{ if(magnifierGlass) magnifierGlass.style.display='none'; });
}

