let currentLang = localStorage.getItem('ms-lang') || 'uz-lat';

function applyLang(lang) {
  if(!T[lang]) return;
  currentLang = lang; localStorage.setItem('ms-lang', lang);
  const t = T[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if(t[key] !== undefined) el.innerHTML = t[key];
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if(t[key] !== undefined) el.placeholder = t[key];
  });

  // Multilingual data attributes (product names, descriptions, categories)
  document.querySelectorAll('[data-name-uz]').forEach(el => {
    const val = (lang==='ru' ? el.dataset.nameRu : lang==='en' ? el.dataset.nameEn : null) || el.dataset.nameUz;
    if(val) el.textContent = val;
  });
  document.querySelectorAll('[data-desc-uz]').forEach(el => {
    const val = (lang==='ru' ? el.dataset.descRu : lang==='en' ? el.dataset.descEn : null) || el.dataset.descUz;
    if(val) el.textContent = val;
  });
  document.querySelectorAll('[data-cat-uz]').forEach(el => {
    const val = (lang==='ru' ? el.dataset.catRu : lang==='en' ? el.dataset.catEn : null) || el.dataset.catUz;
    if(val) el.textContent = val;
  });

  // Color option names and active color label
  document.querySelectorAll('.color-opt[data-name-uz]').forEach(opt => {
    const name = (lang==='ru' ? opt.dataset.nameRu : lang==='en' ? opt.dataset.nameEn : null) || opt.dataset.nameUz;
    opt.dataset.name = name;
    opt.title = name;
    if(opt.classList.contains('active')) {
      const lbl = document.getElementById('colorName');
      if(lbl) lbl.textContent = name;
    }
  });

  // Texture option names and active texture label
  document.querySelectorAll('.texture-opt[data-name-uz]').forEach(opt => {
    const name = (lang==='ru' ? opt.dataset.nameRu : lang==='en' ? opt.dataset.nameEn : null) || opt.dataset.nameUz;
    opt.dataset.name = name;
    opt.textContent = name;
    if(opt.classList.contains('active')) {
      const lbl = document.getElementById('textureName');
      if(lbl) lbl.textContent = name;
    }
  });

  const mtl = document.getElementById('mobileThemeLabel');
  if(mtl) mtl.textContent = isDark ? (t['theme.dark']||'') : (t['theme.light']||'');
  document.querySelectorAll('.lang-btn').forEach(b => b.classList.toggle('active', b.dataset.lang === lang));
}

