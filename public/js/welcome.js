function applyTranslation(lang) {
  const t = translations[lang];
  if (!t) return;
  currentLang = lang;
  localStorage.setItem('ms-lang', lang);

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      el.innerHTML = t[key];
    }
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key]) el.placeholder = t[key];
  });

  // Switch multilingual data attributes (product names, descriptions, categories)
  document.querySelectorAll('[data-name-uz]').forEach(el => {
    const val = (lang==='ru' ? el.dataset.nameRu : lang==='en' ? el.dataset.nameEn : null) || el.dataset.nameUz;
    if(val) el.textContent = val;
  });
  document.querySelectorAll('[data-desc-uz]').forEach(el => {
    const val = (lang==='ru' ? el.dataset.descRu : lang==='en' ? el.dataset.descEn : null) || el.dataset.descUz;
    if(val) el.textContent = val;
  });
  document.querySelectorAll('[data-cat-uz]').forEach(el => {
    const val = (lang==='ru' ? el.dataset.catRu : lang==='en' ? el.dataset.catEn : null) || el.dataset.catUz;
    if(val) el.textContent = val;
  });

  // Active lang buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // counter suffix
  const suffixes = document.querySelectorAll('.counter-suffix');
  if (suffixes[1]) {
    const map = {'uz-lat':' yil','uz-kir':' йил','ru':' лет','en':' yrs'};
    suffixes[1].textContent = map[lang] || ' yil';
  }

  // Update theme button label
  if (mobileThemeLabel) {
    mobileThemeLabel.textContent = isDark
      ? (themeLabels.dark[lang] || themeLabels.dark['uz-lat'])
      : (themeLabels.light[lang] || themeLabels.light['uz-lat']);
  }
}

document.querySelectorAll('.lang-btn').forEach(btn => {
  btn.addEventListener('click', () => applyTranslation(btn.dataset.lang));
});

